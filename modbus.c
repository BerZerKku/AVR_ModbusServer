/**	Подсчет контрольной суммы CRC-16.
 *
 * 	@param[in] *buf Указатель на буфер сообщения.
 * 	@param[in] num Количество байт данных для подсчета КС.
 * 	@return Полученная CRC-16.
 */
uint16_t calcCRC(uint8_t *buf, uint8_t num) {
	uint8_t low = 0xFF;
	uint8_t hi = 0xFF;
	for (uint8_t i = 0, tmp; i < num; i++) {
		tmp = hi ^ *(buf++);
		hi = low ^ pgm_read_byte(&CRC_HI[tmp]);
		low = pgm_read_byte(&CRC_LOW[tmp]);
	}
	return ((hi << 8) | low);
}

// Чтение регистров - 0x03.
uint8_t MODBUS_readHoldingRegisters(uint8_t *buf, uint8_t adrDev,
		uint16_t adrReg, uint8_t numReg) {
	uint8_t len = 0;
	uint16_t crc = 0;

	buf[len++] = adrDev;
	buf[len++] = MODBUS_FUNCTION_READ_HOLDING_REGISTERS;	// Function
	adrReg--;
	buf[len++] = (uint8_t) (adrReg >> 8);		// Starting Adress Hi
	buf[len++] = (uint8_t) adrReg;				// Starting Adress Low
	buf[len++] = 0;								// No. of Registers Hi
	buf[len++] = numReg;						// No. of Registers Low
	crc = calcCRC(buf, len);
	buf[len++] = (uint8_t) (crc >> 8);
	buf[len++] = (uint8_t) crc;

	return len;
}

// Запись регистра - 0х06.
uint8_t MODBUS_writeSingleRegister(uint8_t *buf, uint8_t adrDev,
		uint16_t adrReg, uint16_t val) {
	uint8_t len = 0;
	uint16_t crc = 0;

	buf[len++] = adrDev;
	buf[len++] = MODBUS_FUNCTION_WRITE_SINGLE_REGISTER;		// Function
	adrReg--;
	buf[len++] = (uint8_t) (adrReg >> 8);		// Starting Adress Hi
	buf[len++] = (uint8_t) adrReg;				// Starting Adress Low
	buf[len++] = (uint8_t) (val >> 8);			// Register Value Hi
	buf[len++] = (uint8_t) val;					// Register Value Low
	crc = calcCRC(buf, len);
	buf[len++] = (uint8_t) (crc >> 8);
	buf[len++] = (uint8_t) crc;

	return len;
}

// Проверка принятого сообщения.
MODBUS_STATE_RX MODBUS_checkReadData(uint8_t *bufRx, uint8_t *bufTx, uint8_t len) {
	uint16_t crc = 0;
	MODBUS_STATE_RX state = MODBUS_STATE_RX_OK;

	if (len <= 3) {
		// проверка на минимальную длину принятого сообщения
		return MODBUS_STATE_RX_ERROR_LEN;
	} if (bufRx[0] != bufTx[0]) {
		// проверка совпадения адреса устройства
		return MODBUS_STATE_RX_ERROR_ADR;
	} else if (bufRx[1] != bufTx[1]) {
		// проверка совпадения функции
		return MODBUS_STATE_RX_ERROR_FUNC;
	}

	crc = (bufRx[len - 2] << 8) + bufRx[len - 1];
	if (crc != calcCRC(bufRx, len - 2)) {
		// проверка КС принятого сообщения
		return MODBUS_STATE_RX_ERROR_CRC;
	}

	if (bufRx[1] == MODBUS_FUNCTION_READ_HOLDING_REGISTERS) {
		// для команды чтения регистров проверяется кол-во принятых регистров
		if (bufRx[2] != len - 5) {
			return MODBUS_STATE_RX_ERROR_NUM;
		}
	} else if (bufRx[1] == MODBUS_FUNCTION_WRITE_SINGLE_REGISTER) {
		for(uint8_t i = 2; i <= 5; i++) {
			if (bufRx[i] != bufTx[i]) {
				return MODBUS_STATE_RX_ERROR_DATA;
			}
		}
	}

	return state;
}

